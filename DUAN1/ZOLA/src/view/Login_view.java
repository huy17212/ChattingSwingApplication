    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            User_controller user_controller = new User_controller();
            String sql = "SELECT * FROM Exit_or_not";
            ResultSet rs = jdbc.Helper_JDBC.query(sql, new ArrayList());

            if (checkEmpty() && checkOtherUser() && rs.next() && trueCapcha()) { // 
                user_controller.Insert(new User_model(input_login_tendangnhap.getText(), input_login_password.getText()));
                Menu_view dt = new Menu_view(input_login_tendangnhap.getText(), rs.getInt(1));
                dt.setVisible(true);
                setVisible(false);
                // ghi lại tên để nhớ người dùng
                if (signal == 1) {
                    String sql1 = "UPDATE Who_login set Username_other_user = ?";
                    ArrayList dsProp = new ArrayList<>();
                    dsProp.add(input_login_tendangnhap.getText());
                    jdbc.Helper_JDBC.update(sql1, dsProp);
                } else {
                    String sql1 = "UPDATE Who_login set Username_other_user = ?";
                    ArrayList dsProp = new ArrayList<>();
                    dsProp.add("");
                    jdbc.Helper_JDBC.update(sql1, dsProp);
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Sai thông tin đăng nhập");
                this.jButton1ActionPerformed(evt);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


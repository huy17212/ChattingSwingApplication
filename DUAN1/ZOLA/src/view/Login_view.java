    int n = 0;

    public void Random() {

        Random rand = new Random();
        n = rand.nextInt(4);
        ImageIcon image = new ImageIcon(getClass().getResource("/image_Capcha/" + dsImage.get(n).getImage_capcha()));
        
        Image img = image.getImage();

        image = new ImageIcon(img);
        input_login_Capcha.setIcon(image);
        input_code_capcha.setText("");

    }

    private boolean checkEmpty() {
        if (!input_login_tendangnhap.getText().equals("") && !input_login_password.getText().equals("") && !input_code_capcha.getText().equals("")) {
            return true;
        }
        return false;
    }

    private boolean checkOtherUser() {
        try {
            String sql = "SELECT Username_other_user, Password FROM Other_user";
            ResultSet rs = jdbc.Helper_JDBC.queryNvarchar(sql, new ArrayList());

            while (rs.next()) {
                if (rs.getString(1).equals(input_login_tendangnhap.getText()) && rs.getString(2).equals(input_login_password.getText())) {
                    return true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return false;
    }

    private boolean trueCapcha() {
        if ((input_code_capcha.getText() + " ").equals(dsImage.get(n).getCode_capcha())) {
            return true;
        }

        return false;
    }

    private void LoadWho_login() {
        try {
            String sql = "SELECT * FROM Who_login";
            ArrayList dsProp = new ArrayList();
            ResultSet rs = jdbc.Helper_JDBC.query(sql, dsProp);
            if (rs.next()) {
                if (!rs.getString(1).equals("")) {
                    input_login_tendangnhap.setText(rs.getString(1));
                } else {
                    input_login_tendangnhap.setText("Tên đăng nhập");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


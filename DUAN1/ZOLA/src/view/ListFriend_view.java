        data_user = new Data_User();
        // Insert List_Friend vao DB Chat
        List_friend_model dtMau = new List_friend_model(loginname, Who_give_u);
        list_friend_controller.Insert(dtMau);
        String sql3 = "Delete From [Other_chat]";
        jdbc.Helper_JDBC.update(sql3, new ArrayList<>());

        // B1: Delete DB Send
        insertmssql = new InsertMSSQL();
        insertmssql.DELETE_SEND_TEMP();
        insertmssql.DELETE_LIST_FRIEND_TEMP();

        // B2: Ghi file Send vao DB
        insertmssql.INSERT_SEND_TEMP_TO_DB(Who_give_u);

        // B3: Delete Friend_accept va Send
        friend_accept_controller.Delete(Who_give_u);

        String sql = "DELETE FROM [Send_temp] Where Username_other_user = ?";
        ArrayList dsProp = new ArrayList();
        dsProp.add(loginname);
        jdbc.Helper_JDBC.update(sql, dsProp);

        sql = "INSERT INTO List_friend_temp VALUES (?,?)";
        dsProp = new ArrayList();
        dsProp.add(Who_give_u);
        dsProp.add(loginname);
        jdbc.Helper_JDBC.update(sql, dsProp);
        //B4.5: Xóa FileText Cũ 
        try {
            FileWriter folder_friend_send = new FileWriter("src//data_Mess_Zola//" + Who_give_u + "//Friend_send.txt");
            BufferedWriter BW = new BufferedWriter(folder_friend_send);
            BW.write("");

        } catch (Exception e) {
            e.printStackTrace();
        }
        // B5: Ghi DB moi vao file text
        data_user.WRITE_TO_LIST_FRIEND_TEMP(Who_give_u);
        data_user.WRITE_TO_SEND(Who_give_u);

        // B6: Insert New Text
        insertmssql.INSERT_OTHER_CHAT_TEXT_TO_DB(Who_give_u);
        String sql1 = "INSERT INTO Other_chat (Username_user, Username_other_user, ID_type, [Text], Who_send ) VALUES (?,?,?,?,?)";
        ArrayList dsProp1 = new ArrayList();
        dsProp1.add(Who_give_u);
        dsProp1.add(loginname);
        dsProp1.add(1);
        dsProp1.add("Xin Chào Bạn Nha, Mình Vừa Kết Bạn.");
        dsProp1.add(loginname);
        jdbc.Helper_JDBC.updateNvarchar(sql1, dsProp1);
        data_user.WRITE_TO_OTHER_CHAT(Who_give_u);

        Chat_model dtMau1 = new Chat_model(
                loginname,
                Who_give_u,
                1,
                "Xin Chào Bạn Nha, Mình Vừa Kết Bạn.",
                loginname);
        chat_controller.Insert(dtMau1);
        luachon = 0;
        // B7: Fill Lại từ đầu
        FillToTable();
        ShowDown();
//        Menu_view dtSetUpMenu_view = new Menu_view(loginname, 0);

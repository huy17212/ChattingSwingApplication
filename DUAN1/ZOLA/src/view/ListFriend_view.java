    private void tab_friend_dsMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_friend_dsMouseReleased
        if (evt.getButton() == MouseEvent.BUTTON3) {
            if (evt.isPopupTrigger() && luaChonTab == 0) {
                Accept.setText("Chấp nhận kết bạn");
                Delete.setText("Xóa bạn người dùng này");
                ShowInfo.setText("Xem trang cá nhân");
                Popup_Accept.show(evt.getComponent(), evt.getX(), evt.getY());
            } else if (evt.isPopupTrigger() && luaChonTab == 3) {
                showInfomation.setText("Xem thông tin cá nhân người này");
                popup_Group.show(evt.getComponent(), evt.getX(), evt.getY());
            } else {
                Delete1.setText("Xóa lời mời kết bạn này");
                ShowInfo1.setText("Xem trang cá nhân");
                Popup_Send.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        }
    }//GEN-LAST:event_tab_friend_dsMouseReleased

